#ifndef PORT_TYPES_H
#define PORT_TYPES_H

/**********************************************************************************************************************
 *  INCLUDES
 *********************************************************************************************************************/
#include <stdint.h>

/**********************************************************************************************************************
 *  GLOBAL DATA TYPES AND STRUCTURES
 *********************************************************************************************************************/
typedef struct {
    uint32_t port;
    uint32_t pin;
    uint32_t mode;
    uint32_t pull;
    uint32_t speed;
    uint32_t output_level;
} Port_PinConfigType;

typedef struct {
    Port_PinConfigType const* pin_config_ptr;
    uint32_t pin_config_count;
} Port_ConfigType;

#endif /* PORT_TYPES_H */
/**********************************************************************************************************************
 *  END OF FILE: port_types.h
 *********************************************************************************************************************/
